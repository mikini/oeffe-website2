      function getDaysTillDelivery(date)
      {
        deliveryDayOfWeek = 4;   // Thursdays
        deliveryWeekEven = false // in even weeks

        curDate = (new Date(date?date:Date()));
        deltaDeliveryDayOfWeek=deliveryDayOfWeek-curDate.getDay();
	if (curDate.getDay()==0)
	  deltaDeliveryDayOfWeek-=7;
        if (curDate.getWeek()%2 == (deliveryWeekEven?0:1)) // is this a delivery week?
          if (deltaDeliveryDayOfWeek >= 0)
            deltaDeliveryWeek = 0;
          else
            deltaDeliveryWeek = 2;
        else
            deltaDeliveryWeek = 1;

        daysTillDelivery = deltaDeliveryWeek*7 + deltaDeliveryDayOfWeek;
//        console.log ("deltaDeliveryWeek: "+deltaDeliveryWeek+", deltaDeliveryDayOfWeek:"+deltaDeliveryDayOfWeek+", daysTillDelivery:"+daysTillDelivery);
        return daysTillDelivery;
      }

      function getNextDeliveryDate(date)
      {
        baseDate = date?date:Date();
        nextDelivery = (new Date(baseDate));
        nextDelivery.setDate(nextDelivery.getDate()+getDaysTillDelivery(baseDate));
        //console.log('current week:'+curDate.getWeek()+', current day of week: '+curDate.getDay()+', daysTillDelivery: '+daysTillDelivery+', nextDelivery: '+nextDelivery);
        return nextDelivery;
      }

      function formatDate(date, hour = false)
      {
          return date.getDayOfWeek()+' d. '+date.getDate()+'/'+(date.getMonth()+1)+'-'+date.getFullYear()+(hour?" kl. "+date.getHours():"");
      }

